/** The default theme used when none is specified. */
export const DEFAULT_THEME: ThemeName = "catppuccin-mocha";

/**
 * Returns the list of all available theme names.
 * @returns An array of all registered theme name identifiers.
 */
export function getThemeNames(): ThemeName[] {
  return Object.keys(THEMES) as ThemeName[];
}

/**
 * Resolves a theme by name, falling back to the default.
 * @param name - The theme name to look up.
 * @returns The matching theme, or the default theme if not found.
 */
export function resolveTheme(name: string): Theme {
  const theme = THEMES[name as ThemeName] as Theme | undefined;
  if (theme) {
    return theme;
  }

  return THEMES[DEFAULT_THEME];
}
